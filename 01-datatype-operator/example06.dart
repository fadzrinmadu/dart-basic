void main() {
  var a = 10;
  var b = 4;
  var c = a + b; // operands, operators

  // Arithmetic Operators
  print('Arithmetic Operators');
  var penjumlahan = a + b;
  var pengurangan = a - b;
  var perkalian = a * b;
  var pembagian = a / b;
  var sisa = a % b;

  print(penjumlahan);
  print(pengurangan);
  print(perkalian);
  print(pembagian);
  print(sisa);

  // Equality & Relational Operators
  print('Equality & Relational Operators');
  print(a > b);
  print(a < b);
  print(a == b);
  print(a != b);
  print(a >= b);
  print(a <= b);

  // Logical Operators
  print('Logical Operators');
  var x = true;
  var y = false;

  print(x && y);
  print(x || y);
  print(!x);
}
